package filmes;

public class InfFilmes {
    private String codfilme, nome, genero, distribuidora, anolanc;

    public String getCodfilme() {
        return codfilme;
    }

    public void setCodfilme(String codfilme) {
        this.codfilme = codfilme;
    }
    
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getGenero() {
        return genero;
    }

    public String getAnolanc() {
        return anolanc;
    }

    public void setAnolanc(String anolanc) {
        this.anolanc = anolanc;
    }


    public void setGenero(String genero) {
        this.genero = genero;
    }

    public String getDistribuidora() {
        return distribuidora;
    }

    public void setDistribuidora(String distribuidora) {
        this.distribuidora = distribuidora;
    }
    
}
