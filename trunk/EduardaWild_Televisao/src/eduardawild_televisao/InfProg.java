package eduardawild_televisao;

public class InfProg {
    private String codprog, nomeprog, emissora, tempo, dia;

    public String getCodprog() {
        return codprog;
    }

    public void setCodprog(String codprog) {
        this.codprog = codprog;
    }

    public String getNomeprog() {
        return nomeprog;
    }

    public void setNomeprog(String nomeprog) {
        this.nomeprog = nomeprog;
    }

    public String getEmissora() {
        return emissora;
    }

    public void setEmissora(String emissora) {
        this.emissora = emissora;
    }

    public String getTempo() {
        return tempo;
    }

    public void setTempo(String tempo) {
        this.tempo = tempo;
    }

    public String getDia() {
        return dia;
    }

    public void setDia(String dia) {
        this.dia = dia;
    }
    
    
}
